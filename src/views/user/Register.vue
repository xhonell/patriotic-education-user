          }, 1000)
        }
      })
    }

    const goToLogin = () => {
      router.push('/login')
    }

    return {
      registerForm,
      rules,
      registerFormRef,
      loading,
      countdown,
      sendCode,
      handleRegister,
      goToLogin
    }
  }
}
